    private void btnPathJarsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPathJarsActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY); // Use DIRECTORIES_ONLY for directories, FILES_ONLY for files

        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            configuration.setPathJar(selectedFile.getAbsolutePath());
            lblPathJars.setText(configuration.getPathJar());
        }
    }//GEN-LAST:event_btnPathJarsActionPerformed


    private void searchConfiguration() {
        this.configuration = configurationRepository.getConfiguration();
        if (configuration.getPathJar() != null) {
            lblPathJars.setText(configuration.getPathJar());
        }
        if (configuration.getPathProject()!= null) {
            lblPathProject.setText(configuration.getPathProject());
        }
        if (configuration.getServerDomain() != null) {
            inputServerDomain.setText(configuration.getServerDomain());
        }
    }

